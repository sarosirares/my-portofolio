clc
clear
close all

t = [0.0000; 0.3000; 0.6000; 0.9000; 1.2000; 1.5000; 1.8000; 2.1000; 2.4000; 2.7000; 3.0000];
dV = [0.0048; 0.4512; 0.4524; 0.2916; 0.1212; -0.4220; -0.2943; -0.2205; -0.1222; -0.0963; -0.0611];
V = [0.0031; 0.0639; 0.2146; 0.3214; 0.3966; 0.2860; 0.1892; 0.1203; 0.0741; 0.0465; 0.0273];
mP = [-0.8796; 2.6633; 3.8347; 3.2975; 2.5159; -2.3584; -2.0160; -1.8821; -1.4588; -1.4265; -1.2926];

X = zeros(11, 2);

for i = 1:11
    X(i, :) = [V(i), dV(i)];
end

A = (X.'*X)^(-1) * X.' * mP;

P = A(1)*V + A(2)*dV;

figure
subplot(3,1,1)
plot(t, dV)
xlabel('Time (t)');
ylabel('dV');
subplot(3,1,2)
plot(t, V, '--')
xlabel('Time (t)');
ylabel('V');
subplot(3,1,3)
plot(t, mP, ':')
xlabel('Time (t)');
ylabel('P');

figure
hold on
plot(t, mP, ':')
plot(t, P, 'r')
regress(P, mP)